    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(photocap && (!name.getText().isEmpty()) && (!dob.getText().isEmpty()) && (jRadioButton1.isSelected() | jRadioButton2.isSelected()) && position.getSelectedIndex()>0){
        String url = "jdbc:sqlite:C://database/Prison_Management_Officers.db";
         
         
        String quary="insert into Registor_Officers values(?,?,?,?,?,?,?)";
        try{
        
        Connection con=DriverManager.getConnection(url);
        PreparedStatement st=con.prepareStatement(quary);
        
        FileInputStream fin=new FileInputStream("C:/database/Images/mm.png"); 
        
        st.setInt(1, Integer.parseInt(id.getText()));// beacuse auto incremaent 
        st.setString(2, name.getText());
        st.setInt(3, Integer.parseInt(pin.getText()));
        st.setString(4, dob.getText());
        st.setString(5, radio());
        st.setString(6, (String)position.getSelectedItem());
        st.setBinaryStream(7, fin,fin.available());
        int count =st.executeUpdate();
        System.out.println("Success");
        
        st.close();
        con.close();
        photocap=false;
        setVisible(false);
        Login login=new Login();
        login.setVisible(true);
        }
        catch(Exception e){
            System.out.println(e);
        }
        }
        else{
            JOptionPane.showMessageDialog(this, "Take A Photo Or Fill Successfully");
        }
    
    }//GEN-LAST:event_jButton2ActionPerformed

