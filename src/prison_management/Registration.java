    public void setID(){
        
        String url = "jdbc:sqlite:C://database/Prison_Management_Officers.db";
        String quary="SELECT * FROM Registor_Officers ORDER BY ID DESC LIMIT 1"; //whole Table
        
        try{
        Connection con=DriverManager.getConnection(url);
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery(quary);
        
       
        id.setText(String.valueOf(rs.getInt(1)));
        int rowid=Integer.parseInt(id.getText());
        rowid++;
        id.setText(String.valueOf(rowid));
        
        }catch(Exception e){
            
        }
    }

